function check = inertiaCheck(I)

v = zeros(9,1);

v(1) = I(1,1) + I(2,2) >= I(3,3);
v(2) = I(1,1) - I(2,2) <= I(3,3);
v(3) = I(2,2) + I(3,3) >= I(1,1);
v(4) = I(2,2) - I(3,3) <= I(1,1);
v(5) = I(1,1) + I(3,3) >= I(2,2);
v(6) = I(1,1) - I(3,3) <= I(2,2);
v(7) = I(1,1) >= 2*I(2,3);
v(8) = I(2,2) >= 2*I(1,3);
v(9) = I(3,3) >= 2*I(1,2);

if sum(v) == 9
    check = 1;
else
    check = 0;
end

end